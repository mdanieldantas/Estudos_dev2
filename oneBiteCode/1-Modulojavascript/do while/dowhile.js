// variaveis
let velocidade = 0

// entrada de dados
do {
  alert('Velocidade do veiculo é ' + velocidade + ' km/h')
  velocidade -= 20
} while (velocidade > 0)
alert('velocidade final: ' + velocidade + ' km/h')


//processamento

// saida de resultados




